"""
问题：编写程序，接收以空白符分隔的一串单词作为输入，打印其中只由数字组成的单词。

示例输入：2 cats and 3 dogs.
示例输出：['2', '3']

提示：使用 re.findall() 通过正则表达式查找所有匹配的子串。
"""


"""
正则说明
\d：等价 [0-9]，匹配数字；
\d+：匹配一串连续数字；
\b：单词边界，保证只匹配完整数字单词（避免 a123、123abc 被误匹配）；
re.findall：直接返回所有匹配结果列表，一步完成提取。
"""
import re

s = input()

p = r'\b\d+\b'

res = re.findall(p, s)

print(res)
